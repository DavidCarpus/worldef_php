<h3 style="text-align:center;">WE MISSION</h3>
<p>
The WE Foundation bridges the gap between academic and expert
knowledge and their implementation in local communities around the
world. WE are creating the classrooms, learners and livelihoods of
tomorrow to help societies sow the ideas and grow the solutions to
their most pressing issues. Using toolkits like the WE:SOLVE Labs,
our associates craft workable solutions and build global
collaborations, ultimately generating new market opportunities. By
collecting large amounts of anonymized data from each project, WE
are also building a larger picture which helps us make better
connections and more tailored programs for each new community we
interact with. Essentially, WE help share the best proven practices
across all kinds of borders and boundaries to promote and secure
peace through education.
</p>
