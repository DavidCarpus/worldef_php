<h2>INITIATED</h2>
<p>The first American Football clinic the World Education Foundation conducted was in Buenos Aires, Argentina to bring together youth from two different villages in order to promote social cohesion, healthy lifestyles and core values. With the success of the camp, the WE Foundation went on to delivering the game to 8 other countries around the world.
  </p>
