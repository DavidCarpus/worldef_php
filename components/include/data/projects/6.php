<p>The World Education Foundation has introduced American Football clinics in 9 countries around the world and has been able to introduce the game to over 2,200 youth and adult trainers. Through this initiative the promotion of discipline, hard work, teamwork, effort and general health and fitness are highlighted. Participants are introduced to the sport through specific clinics which focus on the basics; rules, positions and general philosophy of the game. Local athletic coaches participate in a more in depth course which focuses on terminology, strategy and logistics of practices and games. Through the development of the programs, WE look to build tournaments and safe places to approach some of the more pressing issues communities are facing within their region.
  </p>

<h2>FOCUS</h2>
<p>World Education Foundation believes sports is a perfect medium to engage and instill core values within youth. With the founder of the WE -Foundation (Marques Anderson) being a former NFL player, the WE Foundation utilizes the sport of American football in some of the most impoverished and affluent neighborhoods in the world. Through this program youth are able to connect ideas, understand the importance of fitness and health, along with promote social cohesion between cultures, religions or demogrpahic locations.
  </p>


