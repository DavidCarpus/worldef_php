<p>WE: KOLWEZI program aims to generate a current social and economic snapshot of the community Domaine Marial, while identifying possible start-up projects of Income-Generating Activities for alternative livelihoods of the beneficiaries of Good Shepherd Sisters in Kolwezi, DR Congo. The solution is to create a framework around livelihood identification and implementation, while recommending core solutions in thefarming and entrepreneurship sector of the community. The subsequent projects serve to be practical guidelines for expansion of opportunities for skills training and job creation, which will be sustainable for the individual beneficiaries and the community of Kolwezi, DR Congo. Farming activities will benefit 150 current community members, Micro-Enterprise activities will benefit 200 current community women and Solar Energy.
  </p>


<h2>FOCUS</h2>
<p>The World Education Foundation is working with the Good Shepherd Sisters to develop alternative livelihood opportunities for artisanal miners in the Kolwezi township of the DR Congo. Through the integration of Farming, Micro-Enterprise and Renewable Energy activities, WE are building capacity and creating new markets for vulnerable populations within the community, maximizing access to income and skills.
  </p>


