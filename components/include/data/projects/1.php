WE: SOLVE Lab is an open space, technology and design incubator focused on local youth creating local solutions. The ‘why’ is to maximize human and societal improvements in marginalized regions across the world.
<br />
<br />
Local innovators harness the global community to build multiple tech solutions, which solve tangible problems. WE: SOLVE provides local entrepreneurs an environment and resources to explore, create and flourish, concentrating on building technical expertise and soft skills to approach their respective solutions. Proven concepts create livelihood opportunities along with new markets, along with data points to approach problems in multiple regions. To maintain sustainability, value added is reinserted into the program, ensuring a seamless sharing of best practices throughout each WE: SOLVE global node. The Education component is integrated in the process.

<br />

<h2>FOCUS</h2>

Empowering communities in marginalized regions of the world to utilize basic tools and technology that solve problems discovered and validated through participatory development. WE facilitate trans-generational development to approachalable solutions, create livelihoods, and generate shared knowledge to be utilized contextually across borders.
