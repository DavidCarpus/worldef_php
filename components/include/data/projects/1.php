<h2>INITIATED</h2>


WE: SOLVE and IRAQ: RECODED conducted 270 interviews / questionnaires along with preliminary feasibility study with local population, on-the-ground stakeholders and private sector industry leaders.WE also conducted 2 Design Thinking workshops in July (Domiz and Qushtapa refugee camps). Results validated needs and on ramps to creating an ICT skills training program, along with an integrated technology incubator within Syrian refugee camps, IDP camps (internally displaced people) and Host community (main area focus Erbil/ Dohuk).
